# while
count = 0
print('Starting')
while count < 10:
    print(count, '', end = '')
    count += 1
print()
print('Done')

# for
print('Print out values in a range')
for i in range(0, 10):
    print(i, '', end='')
print()
print('Done')

# incremented by 2
print('Print out values in a range')
for i in range(0, 10, 2):
    print(i, '', end='')
print()
print('Done')